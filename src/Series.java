
public class Series
{
	 private String Publisher;
	 private String Title;
	 
  // All the Sets
	 
  public void setPublisher(String pub)
  {
	  Publisher = pub;
  }
  
  public void setTitle(String T)
  {
	  Title = T;
  }
  
  // All the Gets
  
  public String getPublisher()
  {
	  return Publisher;
  }
  
  public String getTitle()
  {
	return Title;
  }
}
